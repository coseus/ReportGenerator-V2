def get_language(report: dict | None) -> str:
    value = str((report or {}).get("report_language") or "en").lower().strip()
    return value if value in TRANSLATIONS else "en"

def t(report_or_lang, key: str, **kwargs) -> str:
    lang = report_or_lang if isinstance(report_or_lang, str) else get_language(report_or_lang)
    text = TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key, TRANSLATIONS["en"].get(key, key))
    return text.format(**kwargs) if kwargs else text
